def test_render_router_report_returns_md_and_html() -> None:
    """The convenience wrapper returns both artifacts in one call."""
    state = _build_state()
    md, html = render_router_report(state, info={"topology": "sequential"})

    assert md.startswith("# Incident Report \u2014")
    assert html.startswith("<!DOCTYPE html>")
    # The case id flows through to the HTML title
    assert str(state.incident_id) in html
    # Sequential topology surfaces in the summary table
    assert "| Topology | sequential |" in md
