def test_markdown_contains_all_core_sections() -> None:
    """A fully-populated state renders every optional section."""
    state = _build_state(proposed_actions=[_build_action()])
    md = render_router_report_md(
        state,
        info={"topology": "parallel", "signals": ["multi_agent"], "wall_clock_ms": 1234.5},
    )

    # Header references the incident id
    assert md.startswith("# Incident Report \u2014 22222222-2222-2222-2222-222222222222")

    # Each section heading is present
    for heading in (
        "## Summary",
        "## Triage Rationale",
        "## Findings",
        "## MITRE ATT&CK Mappings",
        "## Proposed Actions",
    ):
        assert heading in md, f"missing section heading: {heading}"

    # Summary table picks up substrate telemetry from `info`
    assert "| Verdict | true_positive |" in md
    assert "| Confidence | 0.87 |" in md
    assert "| Status | completed |" in md
    assert "| Topology | parallel |" in md
    assert "| Signals | multi_agent |" in md
    assert "| Wall-clock (ms) | 1234.5 |" in md

    # Findings rendered as bullets
    assert "- User alice@example.com logged in from Russia" in md
    assert "- Multiple failed MFA attempts" in md

    # MITRE techniques wrapped in inline code
    assert "- `T1078.004`" in md
    assert "- `T1110.001`" in md

    # Proposed actions table has header + a populated row
    assert "| # | Action | Target | Risk | Approval | Rationale |" in md
    assert "| 1 | disable_user | alice@example.com | high | yes |" in md

    # Footer always emitted
    assert "_Generated by AiSOC router on" in md


def test_markdown_skips_optional_sections_when_empty() -> None:
    """Sections backed by empty lists drop out cleanly."""
    state = _build_state(
        findings=[],
        mitre_mappings=[],
        confidence_basis=[],
        proposed_actions=[],
    )
    md = render_router_report_md(state)

    # Required sections still present
    assert "## Summary" in md
    # Optional sections dropped
    assert "## Findings" not in md
    assert "## MITRE ATT&CK Mappings" not in md
    assert "## Triage Rationale" not in md
    assert "## Proposed Actions" not in md
    assert "## Errors" not in md


def test_markdown_error_section_when_state_has_error() -> None:
    """An error on the state surfaces as a fenced ``Errors`` section."""
    state = _build_state(error="recon sub-agent timed out after 30s")
    md = render_router_report_md(state)

    assert "## Errors" in md
    assert "recon sub-agent timed out after 30s" in md
    # Fenced code block (triple backticks) wraps the error
    assert "```" in md


def test_markdown_escapes_pipes_in_action_rationale() -> None:
    """Pipes inside a rationale must be escaped so the table stays valid."""
    action = _build_action(rationale="alice|bob exfiltrated\nover SSH")
    state = _build_state(proposed_actions=[action])
    md = render_router_report_md(state)

    # Backslash-escaped pipe + newline collapsed to space
    assert "alice\\|bob exfiltrated over SSH" in md
    # Newline inside rationale should not break out of the table row
    actions_lines = [line for line in md.splitlines() if line.startswith("| 1 |")]
    assert len(actions_lines) == 1


def test_markdown_handles_invalid_confidence_gracefully() -> None:
    """A non-numeric confidence shouldn't crash the renderer."""
    state = _build_state()
    # Bypass Pydantic validation by mutating after construction
    object.__setattr__(state, "confidence", "not-a-number")  # type: ignore[arg-type]
    md = render_router_report_md(state)

    assert "| Confidence | 0.00 |" in md


def test_markdown_renders_without_info_dict() -> None:
    """Substrate telemetry is optional — caller may omit ``info``."""
    state = _build_state()
    md = render_router_report_md(state)

    assert "## Summary" in md
    # Topology / signals / wall-clock rows skipped when info is missing
    assert "| Topology |" not in md
    assert "| Signals |" not in md
    assert "| Wall-clock (ms) |" not in md


