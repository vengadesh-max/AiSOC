def test_html_wraps_markdown_with_document_chrome() -> None:
    """The HTML output is a full document with the case id in the title."""
    md = "# Incident Report — case-42\n\n## Summary\n\n| Field | Value |\n|---|---|\n| Verdict | tp |\n"
    html = render_router_report_html(md, case_id="case-42")

    assert html.startswith("<!DOCTYPE html>")
    assert "<title>AiSOC Incident Report \u2014 case-42</title>" in html
    assert "Generated by AiSOC on" in html
    # Markdown library should turn the H1 into an <h1>
    assert "<h1>" in html
    # Table extension should render the pipe-table as a real HTML table
    assert "<table>" in html


def test_html_fallback_when_markdown_module_missing(monkeypatch: pytest.MonkeyPatch) -> None:
    """If ``markdown`` can't be imported we still ship a usable document."""
    import builtins

    real_import = builtins.__import__

    def fake_import(name, *args, **kwargs):  # type: ignore[no-untyped-def]
        if name == "markdown":
            raise ImportError("forced failure for test")
        return real_import(name, *args, **kwargs)

    monkeypatch.setattr(builtins, "__import__", fake_import)

    md = "# Hello\n\nsome body text"
    html = render_router_report_html(md, case_id="case-1")

    # Falls back to wrapping the raw Markdown in <pre>
    assert "<pre># Hello\n\nsome body text</pre>" in html
    # Still produces a complete HTML document
    assert html.startswith("<!DOCTYPE html>")
    assert "</html>" in html


